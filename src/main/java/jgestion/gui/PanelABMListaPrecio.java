   private void numeros_puntos_menos(KeyEvent evt) {
      int k = evt.getKeyChar();
      if ((k < 48 || k > 57) && (k != 45) && (k != 46)) {
         evt.setKeyChar((char) java.awt.event.KeyEvent.VK_CLEAR);
      }
   }

