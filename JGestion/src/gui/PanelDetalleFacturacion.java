    private void cbComprasVentasItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbComprasVentasItemStateChanged
        if (cbComprasVentas.getSelectedIndex() == 0) {
            getCbClieProv().setEnabled(false);
            getCbCuenta().setEnabled(false);
            getCbSubCuenta().setEnabled(false);
            getCbClieProv().removeAllItems();
            getCbCuenta().removeAllItems();
            getCbSubCuenta().removeAllItems();
        } else if (cbComprasVentas.getSelectedIndex() == 1) {
            getCbClieProv().setEnabled(true);
            getCbCuenta().setEnabled(true);
            getCbSubCuenta().setEnabled(true);
            cargarProveedores();
        } else if (cbComprasVentas.getSelectedIndex() == 2) {
            getCbClieProv().setEnabled(true);
            getCbCuenta().setEnabled(true);
            getCbSubCuenta().setEnabled(true);
            cargarClientes();
        }
    }//GEN-LAST:event_cbComprasVentasItemStateChanged
