    @Override
    @SuppressWarnings("SleepWhileInLoop")
    public void run() {
        System.out.println("Connectivity Thread begining..");
        EntityManager em = null;
        while (activa) {
            if (UsuarioController.getCurrentUser() != null) {
                try {
                    em = DAO.getEntityManager();
                    em.createNativeQuery("SELECT * FROM usuario WHERE id = " + UsuarioController.getCurrentUser().getId()).getSingleResult();
                    if (labelConnetionState.getText().length() > 0) {
                        labelConnetionState.setIcon(new javax.swing.ImageIcon(getClass().getResource("/iconos/database_32.png"))); // NOI18N
                        labelConnetionState.setToolTipText("ConexiÃ³n establecida y funcionando");
                        labelConnetionState.setText("");
                    }
                } catch (DatabaseException e) {
                    labelConnetionState.setIcon(new javax.swing.ImageIcon(getClass().getResource("/iconos/database_close_32.png"))); // NOI18N
                    labelConnetionState.setToolTipText("Â¡ERROR DE CONEXIÃN!");
                    labelConnetionState.setText("Â¡ERROR DE CONEXIÃN!");
                    labelConnetionState.setForeground(Color.RED);
                    Logger.getLogger(this.getClass()).log(Level.ERROR, e);
                } catch (Exception e) {
                    labelConnetionState.setEnabled(false);
                    labelConnetionState.setText("Â¡ERROR DE CONEXIÃN!");
                    labelConnetionState.setForeground(Color.RED);
                    labelConnetionState.setToolTipText("Â¡ERROR DE CONEXIÃN!");
                    Logger
                            .getLogger(JFP.class).log(Level.ERROR, "Error en Thread de Chequeo de conexiÃ³n con DB", e);
                    activa = false;
                } finally {
                    try {
                        em.close();
                        em = null;
                        Thread.sleep(3000);


                    } catch (InterruptedException ex) {
                        Logger.getLogger(JFP.class
                                .getName()).log(Level.ERROR, null, ex);
                        JOptionPane.showMessageDialog(
                                this, "SucediÃ³ un error en el hilo de conexiÃ³n, para asegurar un correcto funcionamiento del sistema, cierre y vuelva a abrirlo");
                    }
                }
            }
        }
        System.out.println("Connectivity Thread finished..");
    }

