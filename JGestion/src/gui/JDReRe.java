   /**
    * Setea un mensaje de información.
    * Si @param messageType < -1 || > 3 no hace NADA.
    * @param msg,.. mensaje.
    * @param titulo (puede tener título o no).
    * @param messageType -1=PLAIN, 0=ERROR, 1=INFO, 2=WARRNING.
    */
