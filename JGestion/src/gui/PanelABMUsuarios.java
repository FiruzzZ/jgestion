    private void jPasswordField2FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jPasswordField2FocusLost
      if(jPasswordField1.isEnabled()) {
         if( ! getjPasswordField1().equals(getjPasswordField2())
             || getjPasswordField1().length() < 5) {
            label_Pwd_OK.setIcon(
                 new javax.swing.ImageIcon(getClass().getResource("/iconos/cancel.png")));
         } else {
            label_Pwd_OK.setIcon(
                 new javax.swing.ImageIcon(getClass().getResource("/iconos/accept.png")));
         }
      }
    }//GEN-LAST:event_jPasswordField2FocusLost


