    public void setConfig(VentaSimpleConfig config) {
        UTIL.setSelectedItem(cbCliente, config.getCliente());
        UTIL.setSelectedItem(cbListaPrecio, config.getListaPrecios());
        UTIL.setSelectedItem(cbCaja, config.getCaja());
        UTIL.setSelectedItem(cbUnidadDeNegocio, config.getUnidadDeNegocio());
        UTIL.setSelectedItem(cbSucursal, config.getSucursal());
        UTIL.setSelectedItem(cbCuenta, config.getCuenta());
        UTIL.setSelectedItem(cbSubCuenta, config.getSubCuenta());
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        for (int row = 0; row < dtm.getRowCount(); row++) {
            Rubro r = (Rubro) dtm.getValueAt(row, 0);
            dtm.setValueAt(config.getRubros().contains(r), row, 2);
        }
    }

