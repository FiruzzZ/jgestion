    private void calcularTotal() {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        BigDecimal total = BigDecimal.ZERO;
        for (int row = 0; row < dtm.getRowCount(); row++) {
            BigDecimal x = (BigDecimal) dtm.getValueAt(row, 4);
            total = total.add(x);
        }
        tfTotal.setText(total.toPlainString());
    }

    public void loadRubros(final List<Rubro> rubros) {
        GridLayout layout = new GridLayout(1, 0);
        rubroButtonPanel.setLayout(layout);
        for (Rubro rubro : rubros) {
            final JButton btn = new JButton(rubro.getNombre(), null);
            btn.setFont(new Font("Tahoma", Font.BOLD, 14));
            btn.addActionListener(new ActionListener() {

                @Override
                public void actionPerformed(ActionEvent e) {
                    for (Rubro r : rubros) {
                        if (r.getNombre().equals(btn.getActionCommand())) {
                            buildProductosButtonPanel(r);
                            break;
                        }
                    }
                }

            });
            rubroButtonPanel.add(btn);
        }
    }

    private void buildProductosButtonPanel(Rubro rubro) {
        productosButtonPanel.removeAll();
        GridLayout layout = new GridLayout(0, 4, 10, 10);
        productosButtonPanel.setLayout(layout);
        productos = new ProductoJpaController().findBy(rubro);
        for (Producto p : productos) {
            ImageIcon ii = null;
            if (p.getFoto() != null) {
                if (p.getFoto().length > 0) {
                    ii = new ImageIcon(p.getFoto());
                    Image img = ii.getImage().getScaledInstance(60, 60, java.awt.Image.SCALE_SMOOTH);
                    ii = new ImageIcon(img);
                } else {
                    System.out.println("sin imagen: " + p.toString());
                }
            }
//                File foto = new File("./imgs/" + p.getCodigo() + ".jpg");
//            URL rsc = getClass().getResource("/jgestionclub/imgs/" + p.getCodigo().toLowerCase() + ".jpg");
//            if (rsc == null) {
//                rsc = getClass().getResource("/jgestionclub/imgs/" + p.getCodigo().toLowerCase() + ".png");
//            }
            final JButton btn = new JButton(p.getNombre(), ii);
            btn.setPreferredSize(new Dimension(50, 20));
            btn.setFont(new Font("Tahoma", Font.BOLD, 14));
            btn.addActionListener(new ActionListener() {

                @Override
                public void actionPerformed(ActionEvent e) {
                    for (Producto pp : productos) {
                        if (pp.getNombre().equals(btn.getActionCommand())) {
                            loadProductoInfo(pp);
                            break;
                        }
                    }
                }

            });
            productosButtonPanel.add(btn);
        }
        productosButtonPanel.revalidate();
        productosButtonPanel.repaint();
    }

    private void loadProductoInfo(Producto pp) {
        selectedProducto = pp;
        tfProducto.setText(pp.getNombre());
        tfCantidad.setText("1");
        BigDecimal precio = selectedProducto.getPrecioVenta();
        Double margen = Contabilidad.GET_MARGEN_SEGUN_LISTAPRECIOS(listaPrecio, selectedProducto, precio.doubleValue());
        precio = precio.add(BigDecimal.valueOf(margen));
        tfPrecioUnitario.setText(precio.toPlainString());
    }

