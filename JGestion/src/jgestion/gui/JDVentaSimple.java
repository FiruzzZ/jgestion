    private void btnAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddItemActionPerformed
        if (selectedProducto == null) {
            return;
        }
        Integer ca = Integer.valueOf(tfCantidad.getText());
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        BigDecimal precio = selectedProducto.getPrecioVenta();
        Double margen = Contabilidad.GET_MARGEN_SEGUN_LISTAPRECIOS(listaPrecio, selectedProducto, precio.doubleValue());
        precio = precio.add(BigDecimal.valueOf(margen));
        dtm.addRow(new Object[]{selectedProducto, selectedProducto.getNombre(), ca, precio, precio.multiply(BigDecimal.valueOf(ca))});
        calcularTotal();
    }//GEN-LAST:event_btnAddItemActionPerformed

    private void btnIncreaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncreaseActionPerformed
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            return;
        }
        Integer cant = (Integer) dtm.getValueAt(selectedRow, 2);
        BigDecimal $pre = (BigDecimal) dtm.getValueAt(selectedRow, 3);
        cant++;
        dtm.setValueAt(cant, selectedRow, 2);
        dtm.setValueAt($pre.multiply(BigDecimal.valueOf(cant)), selectedRow, 4);
        calcularTotal();
    }//GEN-LAST:event_btnIncreaseActionPerformed

    private void btnDecreaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDecreaseActionPerformed
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            return;
        }
        Integer cant = (Integer) dtm.getValueAt(selectedRow, 2);
        if (cant == 1) {
            return;
        }
        BigDecimal $pre = (BigDecimal) dtm.getValueAt(selectedRow, 3);
        cant--;
        dtm.setValueAt(cant, selectedRow, 2);
        dtm.setValueAt($pre.multiply(BigDecimal.valueOf(cant)), selectedRow, 4);
        calcularTotal();
    }//GEN-LAST:event_btnDecreaseActionPerformed

